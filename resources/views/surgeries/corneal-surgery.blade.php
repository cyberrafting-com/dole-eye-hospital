                        <h2>Diagnosis of Corneal Conditions</h2>
                        <p>An eye doctor diagnoses corneal conditions during a comprehensive eye exam. The exam may include the following tests:</p>
                        <ul>
                            <li><i class="fa fa-stop-circle" aria-hidden="true"></i>Slit lamp examination. During the examination, the doctor will utilize a slit lamp, essentially a microscope with a powerful light, to examine the cornea and other parts of the eye.</li>
                            <li><i class="fa fa-stop-circle" aria-hidden="true"></i>Fluorescein dye test. This test is used to look for any breaks or tears in the cornea. The doctor will put a drop of fluorescein dye in the eye and then shine a bright light on the eye. The dye will stain any breaks or tears in the cornea, making them easier to see.</li>
                            <li><i class="fa fa-stop-circle" aria-hidden="true"></i>Corneal topography. This test creates a map of the cornea. This map can be used to measure the cornea's thickness and shape and look for any abnormalities.</li>
                            <li><i class="fa fa-stop-circle" aria-hidden="true"></i>Corneal biopsy. This is done under local anesthesia.</li>
                            <li><i class="fa fa-stop-circle" aria-hidden="true"></i>Vision test. This exam evaluates your eyesight and detects any alterations in your vision. The specific tests that are performed will depend on the suspected corneal condition.</li>
