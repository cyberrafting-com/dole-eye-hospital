                        <h2>What is Cataract</h2>
                        <p>A cataract refers to the clouding of the eye's natural lens, located behind the iris (the colored part of the eye). The lens is crucial in focusing light onto the retina, allowing us to see clearly. However, the lens can become progressively opaque and lose its transparency due to aging, prolonged exposure to ultraviolet radiation, certain medications, or underlying medical conditions.</p>
                        <p>Approximately 97% of individuals who undergo cataract surgery experience improved vision due to the procedure's success.</p>
