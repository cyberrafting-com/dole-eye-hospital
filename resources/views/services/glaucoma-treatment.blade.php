                        <h2>Glaucoma Causes</h2>
                        <p>The exact cause of glaucoma is not fully understood. Still, it is generally believed to be related to increased pressure in the eye, known as intraocular pressure (IOP).</p>
                        <p>Pressure buildup can damage the optic nerve, leading to vision loss. However, it is essential to note that not all cases of glaucoma are associated with elevated IOP.</p>
