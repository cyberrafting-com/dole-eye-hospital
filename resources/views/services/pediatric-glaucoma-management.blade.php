                        <h2>Cause Of Pediatric Glaucoma</h2>
                        <ul>
                            <li><strong>1. Congenital Glaucoma:</strong>This form of glaucoma is primarily attributed to a defect in the eye's drainage angle. The drainage angle serves as the passage for fluid to exit the eye. When this angle obstructs, fluid accumulates, increasing intraocular pressure (IOP).</li>
                            <li><strong>2. Juvenile Glaucoma:</strong>Several factors contribute to the development of juvenile glaucoma. It includes congenital glaucoma and eye injuries. Juvenile glaucoma can elevate IOP levels, potentially leading to vision impairment and blindness if not treated promptly.</li>
                            <li><strong>3. Secondary Glaucoma:</strong>Secondary glaucoma arises due to an existing eye condition, such as uveitis (inflammation of the middle eye layer) or cataract (clouding of the eye's lens). Similar to other forms of glaucoma, secondary glaucoma can result in elevated IOP, which, if untreated, may cause vision loss and blindness.</li>
                        </ul>
                    </div>
                    <div class="services-content-inner">
                        <h2>Pediatric Glaucoma Diagnosis</h2>
                        <p>Pediatric Glaucoma Diagnosis will include the following:</p>
                        <ul>
                            <li><strong>1. Measurement of intraocular pressure (IOP):</strong>This is the pressure inside the eye. Elevated IOP is a sign of glaucoma.</li>
                            <li><strong>2. Gonioscopy:</strong>This is a procedure that allows the doctor to see the drainage angle of the eye. The drainage angle is the area where fluid flows out of the eye. If the drainage angle is blocked, this can lead to increased IOP.</li>
                            <li><strong>3. Visual field testing:</strong>This test measures the side vision. Vision loss in the side vision is a sign of glaucoma.</li>
                            <li><strong>4. Optical coherence tomography (OCT):</strong>This test uses sound waves to create a detailed optic nerve image. Damage to the optic nerve is a sign of glaucoma.</li>
                        </ul>
                    </div>
