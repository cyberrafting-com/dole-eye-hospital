                        <h2>What is lazy eye (amblyopia)</h2>
                        <p>Amblyopia, also known as lazy eye, is a type of poor vision that usually happens in just one eye but less commonly in both eyes. It develops when there's a breakdown in how the brain and the eye work together, and the brain can't recognize the sight from one eye. Over time, the brain relies more and more on the other, stronger eye â€” while vision in the weaker eye worsens.</p>
