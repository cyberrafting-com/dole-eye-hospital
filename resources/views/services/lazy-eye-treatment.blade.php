                        <p>Amblyopia is the most common cause of vision loss in children. Up to 3 out of 100 children have it. The good news is that early treatment works well and usually prevents long-term vision problems.</p>
                    </div>
                    <div class="services-content-inner">
                        <h2>There are three main causes of amblyopia:</h2>
                        <ul>
                            <li><strong>1. Strabismus:</strong>This is when the eyes are not aligned properly. The eyes may cross (esotropia), turn outward (exotropia), or tilt upward or downward (hypertropia).</li>
                            <li><strong>2. Refractive error:</strong>This is when the eyes cannot focus properly. The common refractive errors are nearsightedness, farsightedness and astigmatism.</li>
                            <li><strong>3. Deprivation:</strong>This is when there is a problem with how the eye sends signals to the brain. This can happen if there is a cataract, a growth in the eye, or a problem with the retina.</li>
                        </ul>
