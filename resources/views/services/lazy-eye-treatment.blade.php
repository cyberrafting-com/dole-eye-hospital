                        <h2>lazy eye diagnosis</h2>
                        <p>Lazy eye is usually diagnosed in children between 3 and 5 years old. However, it can be diagnosed at any age. The doctor will perform several tests to check the child's vision, including:</p>
                        <ul>
                            <li><strong>1. Visual acuity test:</strong>The child will be asked to read letters or numbers on a chart. Cover test: This test checks how well the child's eyes align when one eye is covered. The doctor will cover each eye individually and observe how the child's eyes move.</li>
                            <li><strong>2. Ophthalmoscopy:</strong>This test allows the doctor to look inside the child's eye.</li>
                            <li><strong>3. Amblyopia screening test:</strong>The test asks the child to follow a moving object with their eyes.</li>
                        </ul>
