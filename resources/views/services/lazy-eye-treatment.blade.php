                        <p>Here are some of the most common treatment options for lazy eye:</p>
                        <ul>
                            <li><strong>1. Patching:</strong>The child wears an eye patch over the stronger eye for several hours daily. This forces the brain to use the weaker eye.</li>
                            <li><strong>2. Vision therapy:</strong>Vision therapy may include exercises that help the child track objects with both eyes, focus on objects at different distances, and see in 3D.</li>
                            <li><strong>3. Glasses or contact lenses:</strong>If the child has a refractive error, glasses or contact lenses can help improve their vision.</li>
                            <li><strong>4. Surgery:</strong>Sometimes, surgery may be required to correct underlying eye conditions that contribute to amblyopia, such as cataracts or strabismus.</li>
