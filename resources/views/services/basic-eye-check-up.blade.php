                        <ul>
                            <li><strong>1. Diabetic Retinopathy:</strong>A condition that affects the blood vessels in the retina, often associated with diabetes.</li>
                            <li><strong>2. Age-Related Macular Degeneration (AMD):</strong>A progressive deterioration of the macula, which can lead to central vision loss.</li>
                            <li><strong>3. Glaucoma:</strong>A group of eye diseases characterized by damage to the optic nerve, often associated with elevated intraocular pressure.</li>
                            <li><strong>4. Retinal Detachment:</strong>A condition where the retina becomes separated from the underlying tissue, requiring immediate medical attention.</li>
                            <li><strong>5. Retinal Tears or Holes:</strong>If left untreated, these can lead to retinal detachment.</li>
                        </ul>
                        <p>Detecting these conditions early through a dilated eye examination can initiate prompt treatment and management, potentially preventing vision loss or minimizing its impact.</p>
