                            <li><i class="fa fa-stop-circle" aria-hidden="true"></i>Darkening of the vision</li>
                            <li><i class="fa fa-stop-circle" aria-hidden="true"></i>Loss of peripheral vision</li>
                            <li><i class="fa fa-stop-circle" aria-hidden="true"></i>Pain in the eye</li>
