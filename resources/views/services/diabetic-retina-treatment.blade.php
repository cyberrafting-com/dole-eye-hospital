                        <h3>Non-proliferative diabetic retinopathy (NPDR):</h3>
                        <p>This is <strong>the early stage of the disease. The symptoms will be mild or nonexistent.</strong> In NPDR, the blood vessels in the retina are weakened.</p>
                    </div>
                    <div class="services-content-inner">
                        <h3>Proliferative diabetic retinopathy (PDR):</h3>
                        <p>PDR is the advanced stage of diabetic retinopathy. New blood vessels grow on the retina, leak blood, and cause blurred vision, floaters, and vision loss.</p>
                    </div>
