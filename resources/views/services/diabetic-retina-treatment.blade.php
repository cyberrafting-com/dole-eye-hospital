                        <h2>Diabetic Retinopathy Diagnosis</h2>
                        <p>The following diagnostic tests are commonly used:</p>
                        <ul>
                            <li><strong>1. Visual acuity test:</strong>Using an eye chart, this test measures how well you can see at various distances.</li>
                            <li><strong>2. Dilated Eye Examination:</strong>The doctor will put drops in your eyes to widen your pupils. They will then examine your retina for signs of damage or abnormalities.</li>
                            <li><strong>3. Tonometry:</strong>This test measures the pressure inside your eyes, vital for detecting diabetic retinopathy.</li>
                            <li><strong>4. Fundus Photography:</strong>In this procedure, special cameras capture detailed retina images, documenting any abnormalities or changes over time.</li>
                            <li><strong>5. Optical Coherence Tomography (OCT):</strong>This is useful for detecting macular edema, a common complication of diabetic retinopathy.</li>
                            <li><strong>6. Fluorescein Angiography:</strong>This test involves injecting a special dye into a vein in your arm, which travels to the blood vessels in your eyes.</li>
                        </ul>
                    </div>
