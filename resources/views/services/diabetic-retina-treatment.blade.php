                        <h2>What is Diabetic retinopathy?</h2>
                        <p>Diabetic retinopathy is a complication of diabetes that affects the eyes. It occurs when high blood sugar levels damage the blood vessels in the retina, the light-sensitive tissue at the back of the eye.</p>
                        <p>The retina converts light into electrical signals that the brain interprets as visual images. Over time, the damaged blood vessels in the retina may leak fluid or blood, leading to various changes in the eye.</p>
